def add_numbers(a: int, b: int) -> int:
    """
    Ajoute deux nombres entiers.

    Args:
        a (int): Le premier nombre.
        b (int): Le second nombre.

    Returns:
        int: La somme des deux nombres.
    """
    return a + b
